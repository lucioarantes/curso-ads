#include <stdio.h>

int main(){
    int a = 10;
    int b = 3;
    int soma = a + b;
    int diferenca = a - b;
    int produto = a * b;
    int quociente = a / b;

    printf("Soma: %d\n", soma);
    printf("Subtração: %d\n", diferenca);
    printf("Multiplicação: %d\n", produto);
    printf("Divisão: %d\n", quociente);

    return 0;
}